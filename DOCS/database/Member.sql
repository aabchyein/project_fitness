
CREATE TABLE MEMBER_INFORMATION
(
  MEMBER_INFORMATION_ID VARCHAR(50) NOT NULL COMMENT 'PK',
  NAME                  VARCHAR(50) NULL     COMMENT '이름',
  USERNAME              VARCHAR(50) NULL     COMMENT '회원ID',
  PASSWORD              VARCHAR(50) NULL     COMMENT '비밀번호',
  BIRTHDATE             VARCHAR(50) NULL     COMMENT '생년월일',
  GENDER                VARCHAR(50) NULL     COMMENT '성별',
  PHONE_NUMBER          VARCHAR(50) NULL     COMMENT '폰번호',
  EMAIL_ADDRESS         VARCHAR(50) NULL     COMMENT '이메일주소',
  ADDRESS               VARCHAR(50) NULL     COMMENT '주소',
  PRIMARY KEY (MEMBER_INFORMATION_ID)
) COMMENT '회원정보';

CREATE TABLE RESERVATION HISTORY
(
  MEMBER_INFORMATION_ID  VARCHAR(50) NOT NULL COMMENT 'FK',
  RESERVATION HISTORY_ID VARCHAR(50) NOT NULL COMMENT 'PK',
  RESERVATION_DATE       VARCHAR(50) NULL     COMMENT '예약날짜',
  RESERVATION_TIME       VARCHAR(50) NULL     COMMENT '예약시간',
  NOTES                  VARCHAR(50) NULL     COMMENT '참고사항',
  CENTER_NAME            VARCHAR(50) NULL     COMMENT '센터명',
  PRIMARY KEY (RESERVATION HISTORY_ID)
) COMMENT '예약내역';

ALTER TABLE RESERVATION HISTORY
  ADD CONSTRAINT FK_MEMBER_INFORMATION_TO_RESERVATION HISTORY
    FOREIGN KEY (MEMBER_INFORMATION_ID)
    REFERENCES MEMBER_INFORMATION (MEMBER_INFORMATION_ID);


CREATE TABLE BOARD
